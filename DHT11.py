import Adafruit_DHT
# Spécifiez le type de capteur DHT et le numéro de broche GPIO utilisé pour la connexion.
sensor = Adafruit_DHT.DHT11
pin = 18

# Essayez de récupérer les données de température et d'humidité à partir du capteur.
humidity, temperature = Adafruit_DHT.read_retry(sensor, pin)

# Si la lecture a réussi, affichez les données.
if humidity is not None and temperature is not None:
    print('Température = {0:0.1f}°C  Humidité = {1:0.1f}%'.format(temperature, humidity))
else:
    print('Échec de la lecture. Réessayer !')
          

